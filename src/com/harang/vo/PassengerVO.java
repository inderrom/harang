package com.harang.vo;

public class PassengerVO {
	
	private String pssg_seat;
	private String ticket_id;
	private String pssg_name;
	private String pssg_bir;
	
	public String getPssg_seat() {
		return pssg_seat;
	}
	public void setPssg_seat(String pssg_seat) {
		this.pssg_seat = pssg_seat;
	}
	public String getTicket_id() {
		return ticket_id;
	}
	public void setTicket_id(String ticket_id) {
		this.ticket_id = ticket_id;
	}
	public String getPssg_name() {
		return pssg_name;
	}
	public void setPssg_name(String pssg_name) {
		this.pssg_name = pssg_name;
	}
	public String getPssg_bir() {
		return pssg_bir;
	}
	public void setPssg_bir(String pssg_bir) {
		this.pssg_bir = pssg_bir;
	}
}
