package com.harang.vo;

public class BlkVO {
	
	private int blk_num;
	private String mem_id;
	private String blk_type;
	private String blk_date;
	private int blk_period;
	
	
	public int getBlk_num() {
		return blk_num;
	}
	public void setBlk_num(int blk_num) {
		this.blk_num = blk_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getBlk_type() {
		return blk_type;
	}
	public void setBlk_type(String blk_type) {
		this.blk_type = blk_type;
	}
	public String getBlk_date() {
		return blk_date;
	}
	public void setBlk_date(String blk_date) {
		this.blk_date = blk_date;
	}
	public int getBlk_period() {
		return blk_period;
	}
	public void setBlk_period(int blk_period) {
		this.blk_period = blk_period;
	}
}
