package com.harang.vo;

public class FacilityVO {
	
	private int fac_num;
	private String airport_id;
	private String fac_name;
	private String fac_pos;
	private String fac_type;
	private String fac_file;
	
	
	public int getFac_num() {
		return fac_num;
	}
	public void setFac_num(int fac_num) {
		this.fac_num = fac_num;
	}
	public String getAirport_id() {
		return airport_id;
	}
	public void setAirport_id(String airport_id) {
		this.airport_id = airport_id;
	}
	public String getFac_name() {
		return fac_name;
	}
	public void setFac_name(String fac_name) {
		this.fac_name = fac_name;
	}
	public String getFac_pos() {
		return fac_pos;
	}
	public void setFac_pos(String fac_pos) {
		this.fac_pos = fac_pos;
	}
	public String getFac_type() {
		return fac_type;
	}
	public void setFac_type(String fac_type) {
		this.fac_type = fac_type;
	}
	public String getFac_file() {
		return fac_file;
	}
	public void setFac_file(String fac_file) {
		this.fac_file = fac_file;
	}
}
