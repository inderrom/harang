package com.harang.vo;

public class TourVO {
	private int tour_num;
	private String airport_id;
	private String tour_name;
	private String tour_phrase;
	private String tour_file;
	
	
	public int getTour_num() {
		return tour_num;
	}
	public void setTour_num(int tour_num) {
		this.tour_num = tour_num;
	}
	public String getAirport_id() {
		return airport_id;
	}
	public void setAirport_id(String airport_id) {
		this.airport_id = airport_id;
	}
	public String getTour_name() {
		return tour_name;
	}
	public void setTour_name(String tour_name) {
		this.tour_name = tour_name;
	}
	public String getTour_phrase() {
		return tour_phrase;
	}
	public void setTour_phrase(String tour_phrase) {
		this.tour_phrase = tour_phrase;
	}
	public String getTour_file() {
		return tour_file;
	}
	public void setTour_file(String tour_file) {
		this.tour_file = tour_file;
	}
	
	
	
}
