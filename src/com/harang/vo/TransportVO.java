package com.harang.vo;

public class TransportVO {
	private String tran_type;
	private String airport_id;
	private String tran_file;
	
	
	public String getTran_type() {
		return tran_type;
	}
	public void setTran_type(String tran_type) {
		this.tran_type = tran_type;
	}
	public String getAirport_id() {
		return airport_id;
	}
	public void setAirport_id(String airport_id) {
		this.airport_id = airport_id;
	}
	public String getTran_file() {
		return tran_file;
	}
	public void setTran_file(String tran_file) {
		this.tran_file = tran_file;
	}
	
	
}
